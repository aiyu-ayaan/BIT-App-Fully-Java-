package com.aatec.bit.Models;

public class Holiday_Model {
    private int sno;
    private String occasion;
    private String date;
    private String day;

    public Holiday_Model() {
    }

    public int getSno() {
        return sno;
    }

    public String getOccasion() {
        return occasion;
    }

    public String getDate() {
        return date;
    }

    public String getDay() {
        return day;
    }
}
